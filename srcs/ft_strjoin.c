#include "ft_printf.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char	*str;
	size_t	i;

	i = 0;
	str = ft_strnew(ft_strlen(s1) + ft_strlen(s2));
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		str[ft_strlen(s1) + i] = s2[i];
		i++;
	}
	return (str);
}
