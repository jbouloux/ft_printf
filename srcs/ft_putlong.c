#include "ft_printf.h"

int				ft_putlong(long n)
{
	char			nb[21];
	unsigned int	i;

	i = 21;
	nb[0] = (n < 0) ? '-' : '+';
	if (n <= 0)
	{
		nb[--i] = '0' - (n % 10);
		n /= -10;
	}
	while (n != 0)
	{
		nb[--i] = '0' + (n % 10);
		n /= 10;
	}
	if (nb[0] == '-')
		nb[--i] = '-';
	return (write(1, nb + i, 21 - i));
}
